def _render_resale_scenario() -> None:
    """情境 2：與房仲看中古屋 / 成屋"""
    st.subheader("🏘️ 情境二：與房仲看中古屋 / 成屋")

    # ---------- 嫌惡設施 1 公里雷達 ----------
    st.markdown("##### 🛰️ 1 公里嫌惡設施雷達 — 看屋前必查")
    st.warning(
        "**走進房子前，先用 Google Map 拉一個 1 公里圓圈——\n"
        "圈內有以下任何一項，租金天花板直接被砍 10-20%：**"
    )

    nuisance_facilities = [
        ("🏭 工廠 / 廢氣排放", "ch11_nuisance_factory"),
        ("⚱️ 靈骨塔 / 墓園", "ch11_nuisance_cemetery"),
        ("🎭 特種行業（酒店、舞廳、八大）", "ch11_nuisance_nightlife"),
        ("🐀 傳統市場（鼠患 / 異味 / 噪音）", "ch11_nuisance_market"),
        ("🛣️ 高架橋 / 鐵路 / 重型車道（噪音）", "ch11_nuisance_traffic"),
        ("🌊 土壤液化區（建築安全疑慮）", "ch11_nuisance_liquefaction"),
    ]

    triggered: list[str] = []
    cols = st.columns(2)
    for idx, (label, key) in enumerate(nuisance_facilities):
        with cols[idx % 2]:
            if st.checkbox(label, key=key):
                triggered.append(label)

    if triggered:
        st.error(
            f"🚫 **抓到 {len(triggered)} 項嫌惡設施！**\n\n"
            + "\n".join(f"- {t}" for t in triggered)
            + "\n\n👉 **這些都會讓租客難找、租金被壓低、轉手變慢。**\n\n"
            "**學長建議**：除非殺到市價 7-8 折，否則**換區再看**。"
        )
    else:
        st.success("✅ 1 公里內**未檢出**重大嫌惡設施，可以實地走訪。")

    st.markdown("---")

    # ---------- 房仲議價反問題 ----------
    st.markdown("##### ⚔️ 房仲議價反問題庫")
    with st.expander("💸 **當房仲要你『加 X 萬就能成交』** → 反擊金句"):
        st.error(
            "**「我已經查過實價登錄與第二類謄本，\n"
            "出價已涵蓋屋主買入本金與剩餘貸款。\n"
            "如果要加價，能否用『**車位升級**』或『**車位樓層調整**』來折抵？」**"
        )
        st.markdown(
            "📚 **學長解釋**：\n"
            "- 直接拒絕加價會讓談判破局；改要**車位升級**（從邊角換到電梯旁）\n"
            "  等於『**沒加錢卻多拿一個 50-100 萬的好處**』\n"
            "- 屋主通常會接受——因為車位『**換位置**』對他而言成本是 0\n"
            "- 你拿到的好車位，**5 年後轉手能多賣 80-150 萬**"
        )

    with st.expander("⏰ **當房仲說『今天不下訂就被搶走』** → 反擊金句"):
        st.error(
            "**「請告訴我『**搶這間的另一組客戶**』的姓名與電話？\n"
            "我可以直接聯絡他們確認——\n"
            "另外，謄本還沒看完，請拿給我看完再說。」**"
        )
        st.markdown(
            "📚 **學長解釋**：『**搶下訂**』99% 是房仲話術，戳破他立刻收斂。"
        )


def _render_inspection_scenario() -> None:
    """情境 3：現場驗屋進行中"""
    st.subheader("🔍 情境三：現場驗屋進行中")

    # ---------- 五神器工具清單 ----------
    st.markdown("##### 🎒 學長五神器工具清單（出門前打勾確認）")
    st.info(
        "**沒帶這 5 樣道具就去驗屋，等於戴著眼罩走平衡木——\n"
        "建商笑著看你簽收，事後修繕費全你出。**"
    )

    tools = [
        ("🔦 **強光手電筒** — 照天花板、牆角、管線縫隙找瑕疵", "ch11_tool_torch"),
        ("📐 **水平尺** — 測量牆角是否完美 90 度、地板是否傾斜", "ch11_tool_level"),
        ("🥤 **1000cc 寶特瓶** — 倒入排水孔測排水速度", "ch11_tool_bottle"),
        ("💡 **小夜燈** — 插每個插座測試是否通電", "ch11_tool_lamp"),
        ("🧻 **衛生紙** — 擦洗手台、馬桶下方 P 管接頭找漏水", "ch11_tool_tissue"),
    ]
    tools_packed: list[bool] = []
    for label, key in tools:
        tools_packed.append(st.checkbox(label, key=key))

    packed_count = sum(tools_packed)
    if packed_count < 5:
        st.warning(f"🟡 工具還缺 {5 - packed_count} 件——**回家再裝一次包**！")
    else:
        st.success("✅ 五神器**全數備齊**，可以出發驗屋！")

    st.markdown("---")

    # ---------- 四大檢測步驟 ----------
    st.markdown("##### 🧪 現場四大檢測步驟（每項都要打勾）")

    inspection_steps = [
        (
            "🚪 **門窗檢測**：門框垂直、門縫 **3-5mm**（過寬會卡塵、過窄會卡死）",
            "ch11_check_door",
            "用水平尺貼著門框檢查；門縫過寬代表房屋有沉陷／傾斜。",
        ),
        (
            "💧 **給排水檢測**：洗手台放滿水，30 秒內**完整排空**",
            "ch11_check_drain",
            "超過 60 秒沒排完 = 管路有阻塞或坡度不足，未來會大幅維修。",
        ),
        (
            "🔬 **抓漏檢測**：用**衛生紙**擦拭洗手台 / 廚房下方 **P 管接頭**",
            "ch11_check_leak",
            "衛生紙變濕 = 微滲漏，雖然眼睛看不到，3 年後就是『**抓漏 30 萬起跳**』。",
        ),
        (
            "🧱 **天地壁檢測**：手敲地磚找『**空心磚**』、水平尺量牆角 **90 度**",
            "ch11_check_walls",
            "空心磚 = 黏著層脫離，未來會碎；牆角不直 = 結構傾斜的最大警訊。",
        ),
    ]

    completed: list[bool] = []
    for label, key, hint in inspection_steps:
        checked = st.checkbox(label, key=key)
        completed.append(checked)
        if checked:
            st.caption(f"📝 {hint}")

    done = sum(completed)
    st.progress(done / len(inspection_steps))
    st.caption(f"驗屋完成度：**{done} / {len(inspection_steps)}**")

    if done == len(inspection_steps):
        st.success(
            "✅ **四大檢測全數完成！**\n\n"
            "👉 **發現問題的處理 SOP：**\n"
            "1. **用手機拍照、錄影留證**（含日期戳記）\n"
            "2. **要求建商 / 屋主在交屋前修繕**（書面確認）\n"
            "3. **未修繕完成前，不撥付交屋款**（依民法 264 條同時履行抗辯）\n"
            "4. 引用**民法第 365 條**：建商交付後 **6 個月內**通知瑕疵保有請求權"
        )
    elif done >= 2:
        st.warning(f"🟡 還有 {len(inspection_steps) - done} 項沒檢測——**別偷懶**！")


